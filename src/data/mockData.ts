export const getYears = (): number[] => {
  const years = [...new Set(pastPapers.map(paper => paper.year))];
  return years.sort((a, b) => b - a);
};

export const getPapersByYear = (year: number): PastPaper[] => {
  return pastPapers.filter(paper => paper.year === year);
};

export const getSubjectByName = (subjectName: string): Subject | undefined => {
  return subjects.find(subject => subject.name === subjectName);
};